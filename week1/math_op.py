import math

print("=== Math Module Functions ===\n")

print("1. PI Constant:")
print(f"   math.pi = {math.pi}")

print("2. Square Root:")
print(f"   sqrt(16) = {math.sqrt(16)}")
print(f"   sqrt(2) = {math.sqrt(2)}\n")

print("3. Cosine Function (in radians):")
print(f"   cos(0) = {math.cos(0)}")
print(f"   cos(π) = {math.cos(math.pi)}")
print(f"   cos(π/2) = {math.cos(math.pi/2)}\n")

print("4. Sine Function (in radians):")
print(f"   sin(0) = {math.sin(0)}")
print(f"   sin(π/2) = {math.sin(math.pi/2)}")
print(f"   sin(π) = {math.sin(math.pi)}\n")